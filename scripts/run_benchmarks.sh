#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "$0")/.." && pwd)"
BUILD_DIR="${ROOT_DIR}/build"
BENCH_BIN="${BUILD_DIR}/edge/cabinos_bench"
OUT_DIR="${ROOT_DIR}/.bench"
mkdir -p "${OUT_DIR}"

ITER_LOCAL="${ITER_LOCAL:-300}"
ITER_CLOUD="${ITER_CLOUD:-30}"

cmake -S "${ROOT_DIR}" -B "${BUILD_DIR}" >/dev/null
cmake --build "${BUILD_DIR}" --target cabinos_bench >/dev/null

"${BENCH_BIN}" --label safety_critical --iterations "${ITER_LOCAL}" --cloud-online 0 --command "turn on hazards" > "${OUT_DIR}/safety.txt"
"${BENCH_BIN}" --label comfort --iterations "${ITER_LOCAL}" --cloud-online 0 --command "set cabin temperature to 22" > "${OUT_DIR}/comfort.txt"
"${BENCH_BIN}" --label cognitive_offline --iterations "${ITER_LOCAL}" --cloud-online 0 --command "find coffee on my route" > "${OUT_DIR}/cognitive_offline.txt"

if [[ -n "${CABINOS_CLOUD_URL:-}" ]]; then
  "${BENCH_BIN}" --label cognitive_online --iterations "${ITER_CLOUD}" --cloud-online 1 --command "find coffee on my route" > "${OUT_DIR}/cognitive_online.txt"
else
  echo "Skipping cognitive_online benchmark (CABINOS_CLOUD_URL not set)."
fi

CABINOS_ROOT_DIR="${ROOT_DIR}" python3 - <<'PY'
import os
from pathlib import Path

root = Path(os.environ.get("CABINOS_ROOT_DIR", ".")).resolve()
out = root / ".bench"
doc = root / "docs" / "benchmark_results.md"

def parse(path: Path):
    d = {}
    if not path.exists():
        return None
    for line in path.read_text().splitlines():
        if "=" in line:
            k, v = line.split("=", 1)
            d[k.strip()] = v.strip()
    return d

rows = []
for name in ["safety", "comfort", "cognitive_offline", "cognitive_online"]:
    p = parse(out / f"{name}.txt")
    if p is None:
        continue
    rows.append(p)

lines = []
lines.append("# Benchmark Results")
lines.append("")
lines.append("Generated by `scripts/run_benchmarks.sh`.")
lines.append("")
lines.append("| Label | Iterations | Avg (ms) | p50 (ms) | p95 (ms) | p99 (ms) |")
lines.append("|---|---:|---:|---:|---:|---:|")
for r in rows:
    lines.append(
        f"| {r.get('label','')} | {r.get('iterations','')} | "
        f"{float(r.get('avg_ms','0')):.2f} | {float(r.get('p50_ms','0')):.2f} | "
        f"{float(r.get('p95_ms','0')):.2f} | {float(r.get('p99_ms','0')):.2f} |"
    )

lines.append("")
lines.append("## Notes")
lines.append("")
lines.append("- `cognitive_online` requires `CABINOS_CLOUD_URL` to be set.")
lines.append("- Results are host and network dependent; rerun for your target environment.")

doc.write_text("\n".join(lines) + "\n")
print(f"Wrote {doc}")
PY

echo "Benchmark outputs:"
ls -1 "${OUT_DIR}"/*.txt
echo "Summary doc: ${ROOT_DIR}/docs/benchmark_results.md"
